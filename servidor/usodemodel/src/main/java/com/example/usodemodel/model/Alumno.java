package com.example.usodemodel.model;

public class Alumno {

    private int id;
    private String nombre;

    public Alumno(){}


    public Alumno(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }
    public String getNombre() {
        return nombre;
    }

    
    
}
